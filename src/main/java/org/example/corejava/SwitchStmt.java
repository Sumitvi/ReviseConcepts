package org.example.corejava;

public class SwitchStmt {

    public static void main(String args[]){

        int age = 30;

        switch (age){
            case 10:
                System.out.println("Age is 10 ");
                break;

            case 20:
                System.out.println("Age is 20 ");
                break;

            case 30:
                System.out.println("Age is 30 ");
                break;

        }

    }
}
